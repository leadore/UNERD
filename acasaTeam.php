 		<h1> ACASA'S TEAM </h1>
    <p> Headed by Jean-Gabriel Ganascia, the ACASA team works on cognitive modeling using symbolic
      artificial intelligence techniques, especially symbolic machine learning, abductive, deductive
      and inductive models of reasoning, semantic information processing, knowledge representation and
      symbolic data fusion. In addition, an axe of research is centered on computational epistemology and 
      on computational ethics. These basic researches lead to a number of applications, including: - Applications 
      to health in collaboration with hospitals, and, more precisely, automatic interpretation of polysomnographic 
      signals using symbolic information fusion techniques; - Protection of the privacy and design of recommendation 
      systems with semantic processing; - Electronic editions, with publishers, making use of semantic processing techniques,
      natural language processing, text mining and cartography of contents. In this area, we have implemented the Labex OBVIL 
      (Observatory of the Literary Life) with various teams of Paris-Sorbonne specialized in literature, which opens our 
      activities onto the "digital humanities''. </p>
      
